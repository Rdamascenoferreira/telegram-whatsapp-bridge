#!/usr/bin/env bash

set -euo pipefail

APP_DIR="${APP_DIR:-/var/www/telegram-whatsapp-bridge}"

sudo apt-get update
sudo apt-get install -y \
  ca-certificates \
  curl \
  git \
  rsync \
  build-essential \
  fonts-liberation \
  libasound2 \
  libatk-bridge2.0-0 \
  libatk1.0-0 \
  libc6 \
  libcairo2 \
  libcups2 \
  libdbus-1-3 \
  libexpat1 \
  libfontconfig1 \
  libgbm1 \
  libglib2.0-0 \
  libgtk-3-0 \
  libnspr4 \
  libnss3 \
  libpango-1.0-0 \
  libpangocairo-1.0-0 \
  libx11-6 \
  libx11-xcb1 \
  libxcb1 \
  libxcomposite1 \
  libxcursor1 \
  libxdamage1 \
  libxext6 \
  libxfixes3 \
  libxi6 \
  libxrandr2 \
  libxrender1 \
  libxss1 \
  libxtst6 \
  xdg-utils

if ! command -v node >/dev/null 2>&1; then
  curl -fsSL https://deb.nodesource.com/setup_20.x | sudo -E bash -
  sudo apt-get install -y nodejs
fi

if ! command -v pm2 >/dev/null 2>&1; then
  sudo npm install -g pm2
fi

mkdir -p "$APP_DIR"
mkdir -p "$APP_DIR/data" "$APP_DIR/.wwebjs_auth" "$APP_DIR/.wwebjs_cache"

echo "Servidor Ubuntu preparado para o deploy em $APP_DIR"
